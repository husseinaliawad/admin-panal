<script>
    document.addEventListener('DOMContentLoaded', function() {
        // Toggle show/hide details (nur eine gleichzeitig)
        document.querySelectorAll('.issue-card').forEach(card => {
            card.addEventListener('click', function(e) {
                // Schließe alle anderen
                document.querySelectorAll('.issue-card').forEach(c => {
                    if (c !== card) c.classList.remove('active');
                });
                // Toggle dieses
                card.classList.toggle('active');
            });
        });
        
        // Optional: Schließe Details beim Klick außerhalb
        document.addEventListener('click', function(e) {
            if (!e.target.closest('.issue-card')) {
                document.querySelectorAll('.issue-card').forEach(c => c.classList.remove('active'));
            }
        });
    });
</script>
